package Switch;

public class Ex2 {
    public static void main(String[] args){
        int day = 4;
        switch (day){
            case 1:
                System.out.println("Saturday");
                break;
            case 2 :
                System.out.println("Sunday");
                break;
            default:
                System.out.println("weekend");
        }
    }
}
